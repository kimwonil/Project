package model;

public class Bank {
	private String no;
	private String bank;
	
	
	public Bank() {
		super();
	}
	public Bank(String no, String bank) {
		super();
		this.no = no;
		this.bank = bank;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getBank() {
		return bank;
	}
	public void setBank(String bank) {
		this.bank = bank;
	}
	
	
	
	
}
